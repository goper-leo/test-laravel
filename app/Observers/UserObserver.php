     * Handle the user "created" event.
     *
     * @param  \App\User  $user
     * @return void
     */
    public function created(User $user)
    {
        // send verification pin on user
        Mail::to($user->email)->send(new VerifyUser($user));

        // Delete invitation record on this email
        Invitation::whereEmail($user->email)->delete();
    }

